package com.developer.couponcode;

public class basicClass {
    String credit;
    int minus;
    public basicClass(String credit, int minus) {
        this.credit = credit;
        this.minus = minus;
    }

    public basicClass() {
    }

    public String getCredit() {
        return credit;
    }

    public int getMinus() {
        return minus;
    }
}
